#include<iostream>
using namespace std;
int main(){
      char c;
      cout<<"enter the character:";
      cin>>c;
      
      switch(c){
      	case 'a':
      		case 'e':
      			case 'i':
      				case 'o':
      					case 'u':
      						cout<<"the character is vowel:"<<endl;
      						break;
      						default:
      							
      						     cout<<"the character is consonant:"<<endl;
	  }
	  return 0;
      
}

